/*
Operadores de comparação:
> - maior que
>= - maior ou igual a
< - menor que
<= menor ou igual a 
=== - igualdade estrita (verifica valor e tipo)
!== - diferente estrito (verifica valor e tipo)
*/

const num1 = 10;
const num2 = '10';

const comparacao = num1 === num2;

console.log(comparacao);

const num3 = 10;
const num4 = 11;

const comp = num3 !== num4;

console.log(comp);
